#include<stdio.h>
/*
三位的水仙花数 
水仙花数153	满足 1*1*1+5*5*5+3*3*3 = 153 
个位数(g)是： %10 
十位数(s)是： /10%10
百位数(b)是： /100 
*/ 
int main(){
	int a=100,g,s,b;			//从哪里开始a就是多少 
	for(a;a<=500;a++){          //到哪里结束a就<=多少  
		g=a%10;
		s=a/10%10;
		b=a/100;
		if((g*g*g+s*s*s+b*b*b)==a){
			printf("%d\t",a);
		}
	} 

	return 0;
}
